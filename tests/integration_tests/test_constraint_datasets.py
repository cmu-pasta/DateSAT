"""
Integration tests for DATE-SMT using constraint datasets.

These tests verify the complete system functionality using real-world constraint
datasets generated by LLM and test both baseline and advanced approaches.
"""

import json
from pathlib import Path

import pytest

from llm_generator.test_template import test_constraints_file as run_constraint_tests


class TestConstraintDatasets:
    """Integration tests using constraint datasets."""

    @pytest.mark.integration
    def test_constraints1_dataset(self, tmp_path):
        """Test constraints1.json dataset with both approaches."""
        constraint_file = Path(__file__).parent / "data" / "constraints1.json"
        output_dir = str(tmp_path / "constraints1_results")

        # Run the integration test
        run_constraint_tests(str(constraint_file), output_dir)

        # Verify results were generated
        assert Path(output_dir).exists()
        assert any(Path(output_dir).glob("results_*.json"))

    @pytest.mark.integration
    def test_constraints2_dataset(self, tmp_path):
        """Test constraints2.json dataset with both approaches."""
        constraint_file = Path(__file__).parent / "data" / "constraints2.json"
        output_dir = str(tmp_path / "constraints2_results")

        # Run the integration test
        run_constraint_tests(str(constraint_file), output_dir)

        # Verify results were generated
        assert Path(output_dir).exists()
        assert any(Path(output_dir).glob("results_*.json"))

    @pytest.mark.integration
    def test_constraint_datasets_consistency(self):
        """Test that both constraint datasets are valid JSON and have expected structure."""
        for dataset_name in ["constraints1.json", "constraints2.json"]:
            constraint_file = Path(__file__).parent / "data" / dataset_name

            with open(constraint_file, 'r') as f:
                constraints = json.load(f)

            # Verify it's a list
            assert isinstance(constraints, list)
            assert len(constraints) > 0

            # Verify each constraint has required fields
            for constraint in constraints:
                assert "id" in constraint
                assert "description" in constraint
                assert "constraint_code" in constraint
                assert "variables" in constraint
                assert "coverage_tags" in constraint
                assert "expected_satisfiable" in constraint

                # Verify constraint_code is executable
                assert isinstance(constraint["constraint_code"], str)
                assert len(constraint["constraint_code"]) > 0

    @pytest.mark.integration
    def test_coverage_tags_diversity(self):
        """Test that constraint datasets cover diverse scenarios."""
        all_tags = set()

        for dataset_name in ["constraints1.json", "constraints2.json"]:
            constraint_file = Path(__file__).parent / "data" / dataset_name

            with open(constraint_file, 'r') as f:
                constraints = json.load(f)

            for constraint in constraints:
                all_tags.update(constraint.get("coverage_tags", []))

        # Verify we have diverse coverage tags
        expected_tags = {
            "leap_boundary",
            "eom",
            "year_vs_days",
            "month_vs_days",
            "chain_add",
            "ineq_window",
            "multi_var",
        }

        # Check that we have most expected tags
        assert (
            len(all_tags.intersection(expected_tags)) >= 5
        ), f"Missing expected coverage tags. Found: {all_tags}"

    @pytest.mark.integration
    def test_satisfiability_predictions(self):
        """Test that satisfiability predictions are reasonable."""
        for dataset_name in ["constraints1.json", "constraints2.json"]:
            constraint_file = Path(__file__).parent / "data" / dataset_name

            with open(constraint_file, 'r') as f:
                constraints = json.load(f)

            sat_count = sum(
                1 for c in constraints if c.get("expected_satisfiable", True)
            )
            total_count = len(constraints)

            # Should have a mix of SAT and UNSAT cases
            assert sat_count > 0, "Should have some satisfiable constraints"
            assert sat_count < total_count, "Should have some unsatisfiable constraints"
