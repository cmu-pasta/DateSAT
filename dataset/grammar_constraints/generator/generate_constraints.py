#!/usr/bin/env python3
"""
Generate and convert constraints from grammar to JSON format.

Steps:
1. Generate constraints using fandango fuzzer
2. Convert from text format to JSON format
3. Clean up temporary files

Input format (generated by fandango):
- Each line contains constraints separated by " ; "
- Variables are D0, D1, D2, etc.
- Example: D0>Date(2019,2,23) ; D1<=((D9-(Period(0,92,84)*408)))

Output format (JSON):
- Array of objects with "id" and "constraints" fields
- Example: {"id": "...", "constraints": ["D0 > Date(2019,2,23)", ...]}
"""

import argparse
import json
import os
import subprocess
import sys
from pathlib import Path


def parse_constraints_file(input_file):
    """
    Parse constraints from text file and convert to JSON format.

    Args:
        input_file: Path to the input constraints.txt file

    Returns:
        List of constraint objects in JSON format
    """
    constraints_list = []

    with open(input_file, "r") as f:
        lines = f.readlines()

    for line_num, line in enumerate(lines, start=1):
        line = line.strip()

        # Skip empty lines
        if not line:
            continue

        # Split by " ; " to get individual constraints
        constraints = [c.strip() for c in line.split(";")]

        # Filter out empty constraints
        constraints = [c for c in constraints if c]

        # Create constraint object
        constraint_obj = {"id": f"grammar-{line_num}", "constraints": constraints}

        constraints_list.append(constraint_obj)

    return constraints_list


def generate_constraints_with_fandango(grammar_file, num_samples, output_file):
    """
    Generate constraints using fandango fuzzer.

    Args:
        grammar_file: Path to the grammar.fan file
        num_samples: Number of constraint sets to generate
        output_file: Path to the output text file

    Returns:
        True if successful, False otherwise
    """
    try:
        cmd = [
            "fandango",
            "fuzz",
            "-f",
            str(grammar_file),
            "-n",
            str(num_samples),
            "-o",
            str(output_file),
        ]

        print(f"Generating constraints with command: {' '.join(cmd)}")
        result = subprocess.run(cmd, check=True, capture_output=True, text=True)

        if result.stdout:
            print(result.stdout)

        print(f"Successfully generated {num_samples} constraint sets")
        return True

    except subprocess.CalledProcessError as e:
        print(f"Error running fandango: {e}")
        if e.stderr:
            print(f"Error output: {e.stderr}")
        return False
    except FileNotFoundError:
        print(
            "Error: 'fandango' command not found. Please ensure fandango is installed and in your PATH."
        )
        return False


def main():
    # Parse command line arguments
    parser = argparse.ArgumentParser(
        description="Generate constraints using fandango and convert to JSON format"
    )
    parser.add_argument(
        "-n",
        "--num-samples",
        type=int,
        default=100,
        help="Number of constraint sets to generate (default: 100)",
    )
    args = parser.parse_args()

    # Get the directory where this script is located
    script_dir = Path(__file__).parent

    # File paths
    grammar_file = script_dir / "grammar.fan"
    temp_file = script_dir / "temp_constraints.txt"
    output_dir = script_dir.parent / "constraints"
    output_file = output_dir / "constraints.json"

    # Check if grammar file exists
    if not grammar_file.exists():
        print(f"Error: Grammar file not found: {grammar_file}")
        sys.exit(1)

    # Create output directory if it doesn't exist
    output_dir.mkdir(parents=True, exist_ok=True)

    # Step 1: Generate constraints using fandango
    print(f"\n{'='*60}")
    print("Step 1: Generating constraints with fandango")
    print(f"{'='*60}")
    if not generate_constraints_with_fandango(
        grammar_file, args.num_samples, temp_file
    ):
        print("Failed to generate constraints. Exiting.")
        sys.exit(1)

    # Step 2: Parse and convert to JSON
    print(f"\n{'='*60}")
    print("Step 2: Converting to JSON format")
    print(f"{'='*60}")
    try:
        print(f"Reading constraints from: {temp_file}")
        constraints_list = parse_constraints_file(temp_file)

        print(f"Writing {len(constraints_list)} constraint sets to: {output_file}")
        with open(output_file, "w") as f:
            json.dump(constraints_list, f, indent=2)

        print("Conversion complete!")

    except Exception as e:
        print(f"Error during conversion: {e}")
        # Clean up temp file even if conversion failed
        if temp_file.exists():
            temp_file.unlink()
        sys.exit(1)

    # Step 3: Clean up temporary file
    print(f"\n{'='*60}")
    print("Step 3: Cleaning up temporary files")
    print(f"{'='*60}")
    try:
        if temp_file.exists():
            temp_file.unlink()
            print(f"Deleted temporary file: {temp_file}")
    except Exception as e:
        print(f"Warning: Could not delete temporary file {temp_file}: {e}")

    # Print summary
    print(f"\n{'='*60}")
    print("SUMMARY")
    print(f"{'='*60}")
    print(f"  Grammar file: {grammar_file}")
    print(f"  Output file: {output_file}")
    print(f"  Constraint sets generated: {len(constraints_list)}")
    print(f"\nAll done!")


if __name__ == "__main__":
    main()
